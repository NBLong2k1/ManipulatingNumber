public class Number {
    int number;
    String result;

    public Number() {
    }

    public Number(int number, String result) {
        this.number = number;
        this.result = result;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
    
    
    
}
